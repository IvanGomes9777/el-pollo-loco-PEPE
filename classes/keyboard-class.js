/**
 * Represents a keyboard input handler.
 * @class
 */
class Keyboard {
  /**
   * Represents the state of the left arrow key.
   * @type {boolean}
   */
  LEFT = false;

  /**
   * Represents the state of the right arrow key.
   * @type {boolean}
   */
  RIGHT = false;

  /**
   * Represents the state of the up arrow key.
   * @type {boolean}
   */
  UP = false;

  /**
   * Represents the state of the down arrow key.
   * @type {boolean}
   */
  DOWN = false;

  /**
   * Represents the state of the spacebar key.
   * @type {boolean}
   */
  SPACE = false;

  /**
   * Represents the state of the 'D' key.
   * @type {boolean}
   */
  D = false;
}

